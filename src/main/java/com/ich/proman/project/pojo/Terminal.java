package com.ich.proman.project.pojo;

public class Terminal {

    private String id;

    private String name;

    public Terminal() {}

    public Terminal(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
